     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        

        $objsubscription              = new Subscription();  
        $objsubscription->email       = $request->email;
        $objsubscription->save(); 

        $lastid = $objsubscription->id; 

        $subscription = Subscription::where('id','=',$lastid)->first();

        event(new UserRegistered($subscription));

         return redirect('/');
    }

